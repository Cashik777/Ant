function getLocaleLines(locale) {
    let start = -1;
    let end = -1;
    let depth = 0;
    for (let i = 0; i < lines.length; i++) {
        if (lines[i].includes(`'${locale}': {`)) {
            start = i;
            depth = 1;
            for (let j = i + 1; j < lines.length; j++) {
                if (lines[j].includes('{')) depth++;
                if (lines[j].includes('}')) depth--;
                if (depth === 0) {
                    end = j;
                    break;
                }
            }
            break;
        }
    }
    return { start, end };
}

