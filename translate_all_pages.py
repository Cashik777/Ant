def update_json_files():
    """Update all locale JSON files with translations"""
    for lang in ["uk", "ru", "en"]:
        path = BASE_DIR / "locales" / f"{lang}.json"
        with open(path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        # Add each page translations
        for page_key, page_trans in ALL_TRANSLATIONS.items():
            if page_key not in data:
                data[page_key] = {}
            for key, translations in page_trans.items():
                data[page_key][key] = translations[lang]
        
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=4)
        
        print(f"Updated {path}")

if __name__ == "__main__":
    print("Adding translations for ALL pages...")
    update_json_files()
